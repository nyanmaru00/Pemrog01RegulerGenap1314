 package Latihan4a.sesi1.bin;
 
 public class CobaOperator01
 {
	public static void main(String[] yanu)
		{
			int x=7;
			System.out.println("Nilai awal X adalah " +x);
			System.out.println("Nilai X++    adalah " + x++);
			System.out.println("Nilai X      adalah " +x);
			System.out.println("");
			int y=25;
			System.out.println("Nilai awal Y adalah " +y);
			System.out.println("Nilai ++Y    adalah " + ++y);
			System.out.println("Nilai Y      adalah " +y);
		}
	}